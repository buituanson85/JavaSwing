    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String name = txt_Category_Name.getText().toString();
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập thông tin");
            
        }else{
            Caterogy caterogy = new Caterogy(name);
            CaterogyModify.insert(caterogy);
            CaterogyModify.saveIntoFile();
            CaterogyModify.readDataFromFile();
        }
        
        txt_Category_Name.setText("");
        showCaterogy();
    }//GEN-LAST:event_btnSaveActionPerformed


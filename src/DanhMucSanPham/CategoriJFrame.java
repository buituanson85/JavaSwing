    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
 
        int index = tableCaterogy.getSelectedRow();
        Caterogy caterogy = caterogyList.get(index);
        
        Connection connection = null;
        PreparedStatement preparedStatement = null;
        
        try {
            connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/Categories", "root", "");
            
            String sql = "update categories set name = ? where id = ?";
            
            preparedStatement = connection.prepareStatement(sql);
            preparedStatement.setString(1, txt_Category_Name.getText().toString());
            preparedStatement.setInt(2, caterogy.getId());
            preparedStatement.execute();
            System.out.println("Sửa thành công");
        } catch (Exception e) {
        }finally{
            if (preparedStatement != null) {
                try {
                    preparedStatement.close();
                } catch (SQLException ex) {
                    Logger.getLogger(CaterogyModify.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if (connection != null) {
                try {
                    connection.close();
                } catch (SQLException ex) {
                    Logger.getLogger(CaterogyModify.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        showCaterogy();
        txt_Category_Name.setText("");
        btnUpdate.setEnabled(false);
        btnDelete.setEnabled(false);
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
       int index = tableCaterogy.getSelectedRow();
       
        if (index >= 0) {
            Caterogy caterogy = caterogyList.get(index);
            
            int option = JOptionPane.showConfirmDialog(this, "do you want to delete this item");
            System.out.println("option: " + option);
            
            if (option == 0) {
                CaterogyModify.delete(caterogy.getId());
                
                showCaterogy();
            }
        }
       txt_Category_Name.setText("");
       btnDelete.setEnabled(false);
       btnUpdate.setEnabled(false);
    }//GEN-LAST:event_btnDeleteActionPerformed

